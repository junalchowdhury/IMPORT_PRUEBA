package Plantillas_Java;

public class Bucles {
	public static void main(String[] args) {
        int i = 0;
        
        //BUCLE WHILE
        while(i<19){
            System.out.println(i);
            i++;
        }
        
        
        //CONDICONAL REPETITIVO
        do{
            System.out.println(i);
            i++;
        }while(i<30);
        
        
        //BUCLE FOR
        for(i=30;i<45;i++){
            System.out.println(i);
        }
        
	}
}
     
